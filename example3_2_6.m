A=zeros(2,6)%创建(2*6)全零数组
A(:)=1:12%赋值符号左边，单下标遍历数组A的全部12个元素。赋值符号右边，拥有12个元素的一维数组
A(2,4)%第2行，第4列的元素
A(8)%单下标情况下，数组A的第8个元素
A(:,[1,3])%双下标的情况下，A的“第1列和第3列上全部行的元素”，在数组的行标识位上，:表示“全部行”
A([1,2,5,6]')%单下标情况下，把数组A的第1,2,5,6个元素排成列向量
A(:,4:end)%双下标的情况下，显示数组A的“从第4列起，到最后一列上全部行的元素”，end用于“标识列”，表示最后一列
A(2,1:2:5)=[-1,-3,-5]%将右边的3个数字分别赋值给A数组第2行的第1,3,5个元素位置，注意赋值符号两边的元素数目相等
B=A([1,2,2,2],[1,3,5])%将数组A的1,3,5列的第1行的元素作为B的第1行；将数组A的1,3,5列的第2行作为B的第2,3,4行
L=A<3%逻辑运算
A(L)=NaN%将符合逻辑条件的位置上，对应的元素设置为NaN