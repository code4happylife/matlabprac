x0=0:0.1:1;%给定数据对，(x0,y0)
y0=[-.447,1.978,3.11,5.25,5.02,4.66,4.01,4.58,3.45,5.35,9.22];
%求拟合多项式
n=3;%假设数据对(x0,y0)一共有m对，则通常情况下，应当保证n<m.观察数据图形的曲线，有助于适当阶数的确定。
P=polyfit(x0,y0,n)%求得x0,y0数组所给数据的n阶拟合多项式系数向量p,为了保证较好的拟合效果，多项式的阶数要取得适当，过低，残差较大；过高,拟合模型将包含噪声影响。
%图示拟合情况
xx=0:0.01:1;
yy=polyval(P,xx);
plot(xx,yy,'-b',x0,y0,'.r','MarkerSize',20)
legend('拟合曲线','原始数据','Location','SouthEast')
xlabel('x')